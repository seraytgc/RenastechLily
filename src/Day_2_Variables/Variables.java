package Day_2_Variables;

public class Variables {
    public static void main(String[] args) {

        // They have already defined in computer,

//      boolean---> true or false-----1 bit
        boolean bolvalue = true;
        System.out.println(bolvalue); //true
        bolvalue = false;
        System.out.println(bolvalue); //false

//      byte------>8 bit (-128 to 127)
        byte bytvalue = 120;
        System.out.println(bytvalue); //120

//      char------>character 'z' 16 bytes
        char charvalue = 'a';
        System.out.println(charvalue); //a

//      short-----> -32768 to 32767
        short shortvalue = 4000;
        System.out.println(shortvalue); //4000

//      int-------> short or integers. 32 bits
        int intvalues = 40000;
        System.out.println(intvalues); //40000

//      long------>64 bits
        long longvalues = 23489575l; // put l at the end of the value, otherwise, it takes the value as int
        System.out.println(longvalues); //23489575

//      float----->32 bits ; holds 7 decimals digit
        float floatvalues = 123.7f;  // you have add f
        System.out.println(floatvalues);//123.7
        float floatvalue2 = 123.789038494f; //123.78904
        System.out.println(floatvalue2);

//      double---->64 bits ; holds 16 digit after decimal point
        double doublevalues = 123.7;
        System.out.println(doublevalues); //123.7





        }













    }

