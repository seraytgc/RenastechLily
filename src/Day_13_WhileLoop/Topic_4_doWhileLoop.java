package Day_13_WhileLoop;

public class Topic_4_doWhileLoop {
    public static void main(String[] args) {
        //the concept :
        //do{
        // code block }
        // while(condition);

//do while
        int i = 0;
        do {
            System.out.println("The i value: " + i); //0-9 //
            i++;
        } while (i < 10); // when it turns false exit>>> exit point
//while
        int j = 0;
        while (j < 10) {
            j++;
        }



    }


}

