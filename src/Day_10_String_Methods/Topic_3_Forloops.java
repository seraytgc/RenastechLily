package Day_10_String_Methods;

public class Topic_3_Forloops {
    public static void main(String[] args) {
        //loops will help you on continuous actions

        //in the loop
        //you will always need to specify
        //starting point ; ending point ; iteration

        for (int i = 0; i < 20; i++) {
            System.out.println("Iteration: " + i);
        }
        for (int i = 20; i >= 0 ; i--) {
            System.out.println("Decrase: " + i);

        }
    }
}
